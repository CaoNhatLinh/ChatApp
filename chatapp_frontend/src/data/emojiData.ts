// Recent emojis (stored in localStorage)
export const getRecentEmojis = (): string[] => {
  try {
    const recent = localStorage.getItem('recentEmojis');
    return recent ? (JSON.parse(recent) as string[]) : [];
  } catch {
    return [];
  }
};

export const addRecentEmoji = (emoji: string): void => {
  try {
    const recent = getRecentEmojis();
    const newRecent = [emoji, ...recent.filter(e => e !== emoji)].slice(0, 24);
    localStorage.setItem('recentEmojis', JSON.stringify(newRecent));
  } catch {
    // Ignore errors
  }
};

